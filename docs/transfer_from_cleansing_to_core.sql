         FROM CLEANSING.L_QUESTION_QUIZ
         WHERE NOT EXISTS (SELECT 1 FROM L_Question_Quiz WHERE question_id = QUESTION_ID AND quiz_id = QUIZ_ID AND sourcesystem = SOURCESYSTEM);

